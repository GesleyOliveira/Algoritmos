"""Faça um fluxograma que, depois de
receber o salário de um funcionário,
calcula e mostra o novo salário,
considerando um aumento de
15,3%"""

salario = float(input("Digite o seu salário"))
novo_salario = salario * 1.153
print(f" O seu salário de R$ {salario} com aumento de 15,3%, será de R$ {novo_salario}")